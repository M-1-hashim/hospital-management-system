// ============================================================
// HMS Theme System — 8 Color Palettes
// Each theme defines ALL CSS custom properties for light & dark.
// ============================================================

export interface ThemeColors {
  /** oklch values for every CSS variable */
  background: string;
  foreground: string;
  card: string;
  'card-foreground': string;
  popover: string;
  'popover-foreground': string;
  primary: string;
  'primary-foreground': string;
  secondary: string;
  'secondary-foreground': string;
  muted: string;
  'muted-foreground': string;
  accent: string;
  'accent-foreground': string;
  destructive: string;
  border: string;
  input: string;
  ring: string;
  'chart-1': string;
  'chart-2': string;
  'chart-3': string;
  'chart-4': string;
  'chart-5': string;
  sidebar: string;
  'sidebar-foreground': string;
  'sidebar-primary': string;
  'sidebar-primary-foreground': string;
  'sidebar-accent': string;
  'sidebar-accent-foreground': string;
  'sidebar-border': string;
  'sidebar-ring': string;
}

export interface HMSColormode {
  light: ThemeColors;
  dark: ThemeColors;
}

export interface HmsTheme {
  id: string;
  label: string;
  labelFa: string;
  /** Tailwind gradient classes for the logo icon background */
  logoGradient: string;
  /** Preview swatch — 3 color stops shown in the theme picker */
  swatches: [string, string, string];
  colors: HMSColormode;
}

