export const ALL_THEMES: HmsTheme[] = [
  emerald,
  ocean,
  indigo,
  purple,
  teal,
  sunset,
  rose,
  crimson,
  slate,
];

export const THEME_MAP = new Map(ALL_THEMES.map((t) => [t.id, t]));

/** Apply theme CSS variables to the document root */
export function applyThemeColors(themeId: string, mode: 'light' | 'dark'): void {
  const theme = THEME_MAP.get(themeId);
  if (!theme) return;

  const vars = mode === 'dark' ? theme.colors.dark : theme.colors.light;
  const root = document.documentElement;

  // Apply every variable as an inline style on :root
  // This allows per-mode switching without class manipulation
  Object.entries(vars).forEach(([key, value]) => {
    const cssVar = `--${key}`;
    root.style.setProperty(cssVar, value);
  });
}
